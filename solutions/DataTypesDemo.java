package solutions;
public class DataTypesDemo {
    public static void main(String[] args) {
        String firstName = "John";       // Storing first name (String)
        double discountedPrice = 19.99;  // Discounted price (double)
        int juiceBottles = 5;            // Number of juice bottles (int)
        int milesTraveled = 120;         // Number of miles traveled (int)
        double highestScore = 95.5;      // Highest test score (double)

        System.out.println("First Name: " + firstName);
        System.out.println("Discounted Price: $" + discountedPrice);
        System.out.println("Juice Bottles: " + juiceBottles);
        System.out.println("Miles Traveled: " + milesTraveled);
        System.out.println("Highest Test Score: " + highestScore);
    }
}
